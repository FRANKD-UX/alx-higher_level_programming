#!/usr/bin/python3
"""
This module provides basic arithmetic operations.
"""


def add(a, b):
    """
    Addition function.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    return a + b


def subtract(a, b):
    """
    Subtraction function.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The difference between a and b.
    """
    return a - b
